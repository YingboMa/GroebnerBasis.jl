    @testset "qq_l2" begin

        n = 4
        R, X = Singular.PolynomialRing(
            Singular.QQ,
            ["x$i" for i in 1:n],
            ordering = :degrevlex
        )
        id = let R=R, (x1, x2, x3, x4) = X
            gens = [
                x1+2*x2+2*x3+2*x4-1,
                x1^2+2*x2^2+2*x3^2+2*x4^2-x1,
                2*x1*x2+2*x2*x3+2*x3*x4-x2,
                x2^2+2*x1*x3+2*x2*x4-x3
            ]
            Singular.Ideal(R, gens)
        end
        sols = msolve(id, la_option=2)
